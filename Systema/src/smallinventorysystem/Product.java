    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        Random ran = new Random();
        int id = Math.abs((ran.nextInt() / 2) * 2);
        if (productName.getText().isEmpty() || productQuantity.getText().isEmpty() || productPrice.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "PLEASE FILL ALL FIELD", "Message", JOptionPane.INFORMATION_MESSAGE);
        } else {
            ManageProduct.addRow(new Object[]{id, productName.getText(), productQuantity.getText(), productPrice.getText()});
            JOptionPane.showMessageDialog(this, "PRODUCT ADDED SUCCESSFULY", "Message", JOptionPane.INFORMATION_MESSAGE);
            productName.setText(null);
            productQuantity.setText(null);
            productPrice.setText(null);
    }//GEN-LAST:event_submitActionPerformed
    }
    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        int choice = JOptionPane.showConfirmDialog(this, "ARE YOU SURE YOU WANT TO DELETE THIS DATA?", "Message", JOptionPane.YES_NO_OPTION);
        if (choice == 0) {
            ManageProduct.removeRow(tableManageProduct.getSelectedRow());
            productName.setText(null);
            productQuantity.setText(null);
            productPrice.setText(null);
            submit.setEnabled(true);
        }
    }//GEN-LAST:event_deleteActionPerformed

