    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        if (!productName.getText().isEmpty() || !productQuantity.getText().isEmpty() || !productPrice.getText().isEmpty()) {

            int selected = tableManageProduct.getSelectedRow();
            tableManageProduct.setValueAt(productName.getText(), selected, 1);
            tableManageProduct.setValueAt(productQuantity.getText(), selected, 2);
            tableManageProduct.setValueAt(productPrice.getText(), selected, 3);
            JOptionPane.showMessageDialog(this, "CATEGORY ADDED SUCCESSFULY", "Message", JOptionPane.INFORMATION_MESSAGE);
            productName.setText(null);
            productQuantity.setText(null);
            productPrice.setText(null);
            submit.setEnabled(true);
        }

    }//GEN-LAST:event_editActionPerformed

    private void tableManageProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableManageProductMouseClicked
        // TODO add your handling code here:
        submit.setEnabled(false);
        String categoryName = ManageProduct.getValueAt(tableManageProduct.getSelectedRow(), 1).toString();
        String categoryQuantity = ManageProduct.getValueAt(tableManageProduct.getSelectedRow(), 2).toString();
        String categoryPrice = ManageProduct.getValueAt(tableManageProduct.getSelectedRow(), 3).toString();

        productName.setText(categoryName);
        productQuantity.setText(categoryQuantity);
        productPrice.setText(categoryPrice);
    }//GEN-LAST:event_tableManageProductMouseClicked

